package chapter01;

public class PrintName {
    public static void main(String[] args) {
        // 1. 한줄에 한 글자씩 자신의 이름을 표시하는 프로그램을 작성
        System.out.println("이");
        System.out.println("승");
        System.out.println("종");

        // 2. 성과 이름 사이에 한번 더 개행하기 (개행문자 또는 println)
        System.out.println("이");
        System.out.println();
        System.out.println("승종");
    }
}
